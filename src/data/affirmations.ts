export const getAffirmationsByCategory = (category: Category): Affirmation[] => {
  return AFFIRMATIONS.filter((a) => a.category === category);
};

export const getRandomAffirmation = (categories?: Category[]): Affirmation => {
  const filtered = categories && categories.length > 0
    ? AFFIRMATIONS.filter((a) => categories.includes(a.category))
    : AFFIRMATIONS;
  const index = Math.floor(Math.random() * filtered.length);
  return filtered[index];
};

export const getCategoryById = (id: Category): CategoryInfo | undefined => {
  return CATEGORIES.find((c) => c.id === id);
};
